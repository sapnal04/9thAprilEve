package finalstudytounderstand;

public class Forloop {

	public static void main(String[] args) {
		
		System.out.println("Printing 1 to 10");
		for(int a=1;a<=10;a++)
			{System.out.println(a);}
		
		System.out.println("printing 10 to 1");
		for(int a=10;a>=1;a--)
		{System.out.println(a);}
		
		System.out.println("Table of 2");
		for(int a=2;a<=20;a=a+2)
		{System.out.println(a);}
		
		System.out.println("Print my name");
		for(int a=1;a<=10;a++)
		{System.out.println("Sapna");}

		System.out.println("Print 0 to 9");
		for(int a=0; a<=9; a++)
		{System.out.println(a);}
	}

}
